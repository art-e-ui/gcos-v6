// Handle Periodic Background Sync
// eslint-disable-next-line @typescript-eslint/no-explicit-any
self.addEventListener('periodicsync', (event: any) => {
  if (event.tag === 'fetch-latest-products') {
    console.log('Fetching latest products in background...');
    // Implementation for periodic sync would go here
  }
});

// Explicit fetch listener to help PWA detection tools
self.addEventListener('fetch', (event) => {
  // Workbox handles most of this via registerRoute, 
  // but having an explicit listener can sometimes help detection
  if (event.request.mode === 'navigate') {
    event.respondWith(
      fetch(event.request).catch(() => {
        return caches.match('/offline.html', { ignoreSearch: true })
          .then(response => response || caches.match('/index.html', { ignoreSearch: true }))
          .then(response => response || Response.error());
      })
    );
  }
});

// Allow the service worker to take control of the page immediately
self.addEventListener('install', () => {
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(self.clients.claim());
});

self.addEventListener('message', (event) => {
  if (event.data && event.data.type === 'SKIP_WAITING') {
    self.skipWaiting();
  }
});
